            <p><img src="./images/zumba.webp" alt="fitness club" width="350"></p>

            <p>
                <p id="zumba"><br><b> Cours de Zumba : </b><br><br>
                    La Zumba est une méthode de fitness combinant exercices physiques, danses et musiques latines.
                    Son objectif : brûler des calories ou simplement garder la forme tout en s'amusant. Fini l'ennui des cours traditionnels de gym !
                    C'est par pur hasard que la méthode est mise au point dans les années 90 par Alberto Perez (dit « Beto » Perez), professeur colombien
                    de fitness, alors qu'il oublie sa musique lors d'un cours (voir notre encadré).La Zumba combine plusieurs danses latines comme la
                    salsa, le merengue, la samba, la cumbia, le reggaeton mais aussi des danses africaines et indiennes.
                    Elle inclut des exercices de résistance permettant de développer la force et la taille des muscles du squelette.
                    La méthode se décline en plusieurs formules, pour tous les âges et les objectifs (perdre du poids, renforcer les muscles, sculpter sa silhouette, etc.)</p>

