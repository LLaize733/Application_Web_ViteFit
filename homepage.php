        <p>
            <p id="Bodytraining"><br><b> Cours de bodytraining : </b><br><br>
                Cours de renforcement musculaire. L'objectif est d'affiner et tonifier tous les muscles du corps avec des séries longues.
                Le cours se pratique avec une barre et des poids libre sur des musiques tendance pour un maximum de motivation.
                Le body training (aussi appelé circuit training généralisé de musculation) est une méthode qui consiste à travailler l’ensemble des chaînes musculaires au
                cours d’un seul entraînement. Bras, dos, ventre, taille, cuisses, fessiers.
                C'est un cours collectif qui permet de se renforcer dans une bonne ambiance, pour un corps plus ferme.</p><br>
            <p>
                Durée : 1h30<br>
                Equipement: fournis <br>
                Nombre de personnes max: 15 <br>
                Prix: 25 €
            </p>
