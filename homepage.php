        <h3>Nos cours : </h3>
		
        <p><img src="./images/cycling.webp" alt="fitness club" width="350"></p>

        <p id="cycling"><br><b>Cours de cycling:</b><br><br>
            Le cycling est un cours de cyclisme indoor, qui consiste en l’utilisation de vélos fixes en salle, et en groupe, le tout en musique.
            Encadrés par un coach sportif, les exercices proposés sont basés sur la résistance cardio respiratoire et le travail musculaire (essentiellement le bas du corps).
            Les intensités sont variées, afin de simuler un parcours de vélo précis. Cette discipline est très efficace en guise d’entraînement, pour préparer une sortie à vélo ou une randonnée,
            mais aussi pour perdre rapidement du poids. Le cours ne comporte aucun mouvement technique, ni de difficulté motrice particulière. Il ne cause aucun impact au niveau ostéo articulaire,
            il est donc accessible même aux personnes qui ont un dos fragile.</p><br>
        <p>
            Durée : 2h<br>
            Equipement: fournis <br>
            Nombre de personnes max: 15 <br>
            Prix: 40 €
        </p>
